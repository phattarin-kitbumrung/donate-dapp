const contract = new web3.eth.Contract(contractABI, contractAddress)
let currentAccount
web3.eth.getAccounts(function(err, accounts) {
  if (err != null) {
    alert("Error retrieving accounts.")
    return
  }
  if (accounts.length == 0) {
    alert("No account found! Make sure the Ethereum client is configured properly.")
    return
  }
  currentAccount = accounts[0]
  console.log('currentAccount: ' + currentAccount)
  web3.eth.defaultAccount = currentAccount
})

//smart contract functions
function sendDonate() {
  const amount = $("#newAmount").val()
  contract.methods.sendViaCall().send({from: currentAccount, value: Number(web3.utils.toWei(amount, 'ether'))}).then(function(tx) { 
    console.log("Transaction: ", tx)
  })
  $("#newAmount").val('')
}

function totalAmount() {
  contract.methods.getBalance().call().then(function(amount) { 
    console.log("amount: ", amount)
    document.getElementById('lastAmount').innerHTML = `${Web3.utils.fromWei(amount, 'ether')} ETH`
  })    
}

